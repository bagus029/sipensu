<h4><b><u>Perencanaan dan Sistem Informasi</u></b></h4>
<h5><p>Perencanaan dan Sistem Informasi (PSI) merupakan bagian dari organisasi di Fakultas Ekonomi dan Bisnis UNSOED. Perencanaan dan Sistem Informasi mempunyai struktur organisasi yang masing-masing menyelenggarakan sistem pelayanan Universitas dan pelayanan akses terhadap infrastruktur Teknologi Informasi dan lingkungannya. PSI Fakultas Ekonomi dan Bisnis UNSOED mempekerjakan sejumlah tenaga spesialis di bidang Teknologi Informasi .</h5>

