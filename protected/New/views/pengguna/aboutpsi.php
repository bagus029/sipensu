<h4><p><b>Keterangan:</b></p></h4>
<h5><p>Semua aspek sehubungan dengan sistem aplikasi yang dibuat dan dikembangkan oleh tim PSI-FEB ini yang meliputi desain, teks, grafis, aplikasi, perangkat lunak dan sumber kode adalah hak cipta dari FEB UNSOED kecuali disebutkan. Tidak ada bagian dari isi atau materi yang tersedia di sistem aplikasi yang dibuat dapat direproduksi, berlisensi, dijual, diterbitkan, ditransmisikan, dimodifikasi, diadaptasi, ditampilkan kepada publik, menyiarkan (termasuk penyimpanan dalam media apapun melalui sarana elektronik atau tidak transiently untuk tujuan apapun disimpan sebagai diizinkan di sini) tanpa izin tertulis dari Fakultas.</p></h5>
